namespace ResumeTech.Domain.Resumes; 

public enum ResumeSectionType {
    Headline,       // Name + contact info
    Summary,        // Short paragraph Intro / mission
    Skills,         // List of relevant skills
    Education,      // List of schools attended / certifications held
    Experience,     // List of past and present jobs
    Projects,       // List of notable projects
    Links           // Collection of links to external sites, like LinkedIn
}